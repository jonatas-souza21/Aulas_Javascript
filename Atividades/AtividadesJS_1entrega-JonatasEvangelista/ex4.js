// Crie um variável preço e atribua a ela um valor numérico.
let valor = 100;

// Em seguida, crie um variável desconto e atribua a ela um valor 0.2, representando a porcentagem (20%) de desconto que você deseja aplicar.
let desconto = 100 * 0.2;

// Crie uma variável Preço final que use as variáveis preço e desconto para calcular o preço com desconto e imprima o resultado no console. 
let precoFinal = valor - desconto; 
console.log(`O preço do produto com o devido desconto é ${precoFinal}`)